# Logical AND
x = True
y = False
result_and = x and y
print("Logical AND:", result_and)

# Logical OR
result_or = x or y
print("Logical OR:", result_or)

# Logical NOT
result_not_x = not x
result_not_y = not y
print("Logical NOT for x:", result_not_x)
print("Logical NOT for y:", result_not_y)


#  Expression example for AND operator
print(5>4 and 7) 
print(5>4 and 7 and 11)
print(5<4 and 7)

print('------------')

# Expression example for OR opeartor
print(5>4 or 7)
print(5<4 or 7)
print(5<4 or 7 or 11)


