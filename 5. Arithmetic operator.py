# Addition
result_add = 5 + 3
print("Addition:", result_add)

# Subtraction
result_sub = 10 - 4
print("Subtraction:", result_sub)

# Multiplication
result_mul = 6 * 7
print("Multiplication:", result_mul)

# Division
result_div = 20 / 5
print("Division:", result_div)

# Floor Division (integer division)
result_floor_div = -24 // 5
print("Floor Division:", result_floor_div)

# Modulo (Remainder)
result_modulo = 21 % 5
print("Modulo (Remainder):", result_modulo)

# Exponentiation
result_exp = 2 ** 3
print("Exponentiation:", result_exp)

# Negation (Unary Minus)
result_negation = -10
print("Negation (Unary Minus):", result_negation)
