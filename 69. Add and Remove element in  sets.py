print('---- Add Method ----')

num_set = {1, 2, 3, 4, 5}
num_set.add(6)
print(num_set)

print()

#==================================================================================================================

print('---- Update Method ----')

num_set = {1, 2, 3, 4, 5}
num_set.update([6, 7, 8])
print(num_set)

print()

#==================================================================================================================

print('---- Remove Method ----')

num_set = {1, 2, 3, 4, 5}
num_set.remove(3)
# num_set.remove(10)
print(num_set)

print()

#==================================================================================================================

print('---- Discard Method ----')

num_set = {1, 2, 3, 4, 5}
num_set.discard(3)
num_set.discard(10)
print(num_set)

print()

#==================================================================================================================

print('---- Pop Method ----')

num_set = {1, 2, 3, 4, 5}
popped_element = num_set.pop()
print("Popped Element:", popped_element)
print("Updated Set:", num_set)

print()

