#Using range(stop)
for i in range(5):
    print(i)

print('----------  range(start, stop ) ------------')

# Using range(start,stop)
for i in range(2, 6):
    print(i)


print('----------  range(start, stop, step ) ------------')
#Using range(start,stop,step)
for i in range(1, 10, 2):
    print(i)


print('---------- Negative range(start, stop, step ) ------------')
#Using range(start,stop,step)
for i in range(10, 1, -1):
    print(i)